package calculator

class AddOperation {
    fun operate(number1: Double, number2: Double) = number1 + number2
}

class SubtractOperation {
    fun operate(number1: Double, number2: Double) = number1 - number2
}

class MultiplyOperation {
    fun operate(number1: Double, number2: Double) = number1 * number2
}

class DivideOperation {
    fun operate(number1: Double, number2: Double) = number1 / number2
}
